namespace EdabitCodePractice.Challenges.Edabit.VeryEasyCodeChallenges
{
    internal class IsOddNumber
    {
        public static bool IsOdd(int num)
        {
            if (num % 2 == 0)
                return false;
            else
                return true;
        }
    }
